// Big O notation is a mathematical notation that describes the limiting behavior of a 
// function when the argument tends towards a particular value or infinity.

// Time complexity
//

// An Example, add up to n
// time complexity O(n)


function addUpto(n) {
  let total = 0;
  for (let i = 0; i < n; i++) {
    total += 1;
  }
  return total;
}

// Another solution for same problem
// time complexity O(1)
function addUpTo(n) {
  return n * (n + 1) / 2;
}
